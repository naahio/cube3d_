#include "cub3D.h"

int	atouint8(const char *str)
{
	int	n;

	n = 0;
	while (*str == ' ')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		n = n * 10 + *str - '0';
		if (n > 0xFF)
			return (-1);
		str++;
	}
	return (n);
}
